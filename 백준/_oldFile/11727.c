#include <stdio.h>

#define MAX 1001

int main()
{
    int N
    ,   dp[MAX];
    
    dp[1] = 1;

    scanf("%d", &N);
    for(int i = 2; i <= N; i++)
    {
        if(i % 2 != 0)
            dp[i] = (dp[i - 1] * 2 - 1) % 10007;
        else
            dp[i] = (dp[i - 1] * 2 + 1) % 10007;
    }
    printf("%llu\n", dp[N]);

    return (0);
}

