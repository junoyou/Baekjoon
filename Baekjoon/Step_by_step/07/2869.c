#include <stdio.h>

int main()
{
    int A,  // 낮 이동
        B,  // 밤 미끄러짐
        V;  // 막대기 높이

    scanf("%d %d %d", &A, &B, &V);
    printf("%d\n", (V - B - 1) / (A - B) + 1);

    return 0;
}

